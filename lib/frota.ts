// Mapeamento de conversão Mercosul: dígito -> letra equivalente (posição 4 da placa)
const MERCOSUL: Record<string, string> = {
  '0': 'A', '1': 'B', '2': 'C', '3': 'D', '4': 'E',
  '5': 'F', '6': 'G', '7': 'H', '8': 'I', '9': 'J'
}
const MERCOSUL_REVERSO: Record<string, string> = Object.fromEntries(
  Object.entries(MERCOSUL).map(([k, v]) => [v, k])
)

export function normalizarPlaca(placa: string): string {
  return placa.replace(/[-.,\s]/g, '').toUpperCase()
}

// Converte placa antiga para Mercosul: ABC1234 -> ABC1D34
function antigoParaMercosul(placa: string): string {
  // Padrão antigo: 3 letras + 4 números (ex: EJV1745)
  const match = placa.match(/^([A-Z]{3})(\d)(\d)(\d{2})$/)
  if (!match) return placa
  const letra = MERCOSUL[match[3]]
  if (!letra) return placa
  return `${match[1]}${match[2]}${letra}${match[4]}`
}

// Converte placa Mercosul para antiga: ABC1D34 -> ABC1234
function mercosulParaAntigo(placa: string): string {
  // Padrão Mercosul: 3 letras + 1 número + 1 letra + 2 números (ex: EJV1H45)
  const match = placa.match(/^([A-Z]{3})(\d)([A-J])(\d{2})$/)
  if (!match) return placa
  const digito = MERCOSUL_REVERSO[match[3]]
  if (!digito) return placa
  return `${match[1]}${match[2]}${digito}${match[4]}`
}

// Retorna todas as variações possíveis de uma placa (antiga + mercosul)
function variacoes(placa: string): string[] {
  const vars = new Set([placa])
  vars.add(antigoParaMercosul(placa))
  vars.add(mercosulParaAntigo(placa))
  return Array.from(vars)
}

export function distanciaLevenshtein(a: string, b: string): number {
  const m = a.length, n = b.length
  const dp: number[][] = Array.from({ length: m + 1 }, (_, i) =>
    Array.from({ length: n + 1 }, (_, j) => (i === 0 ? j : j === 0 ? i : 0))
  )
  for (let i = 1; i <= m; i++)
    for (let j = 1; j <= n; j++)
      dp[i][j] = a[i-1] === b[j-1]
        ? dp[i-1][j-1]
        : 1 + Math.min(dp[i-1][j], dp[i][j-1], dp[i-1][j-1])
  return dp[m][n]
}

export function validarPlaca(placaLida: string): {
  status: 'confirmada' | 'provavel' | 'nao_identificada'
  veiculo?: FrotaVeiculo
  placaCorrigida?: string
} {
  const norm = normalizarPlaca(placaLida)
  if (!norm || norm.length < 4) return { status: 'nao_identificada' }

  // 1. Correspondência exata
  const exata = FROTA.find(v => normalizarPlaca(v.placa) === norm)
  if (exata) return { status: 'confirmada', veiculo: exata }

  // 2. Correspondência via conversão Mercosul (antigo <-> novo padrão)
  // Se a placa lida for Mercosul mas a frota tem o formato antigo, ou vice-versa
  const varsLida = variacoes(norm)
  for (const variacao of varsLida) {
    if (variacao === norm) continue
    const mercosulMatch = FROTA.find(v => normalizarPlaca(v.placa) === variacao)
    if (mercosulMatch) {
      // É uma conversão Mercosul válida — confirmar sem alerta
      return { status: 'confirmada', veiculo: mercosulMatch }
    }
  }

  // 3. Busca fuzzy para erros de digitação (distância <= 2, excluindo diferenças Mercosul)
  let melhor: FrotaVeiculo | undefined
  let menorDist = Infinity
  let placaCorrigida = ''

  for (const v of FROTA) {
    const normFrota = normalizarPlaca(v.placa)
    // Calcular distância considerando também as variações Mercosul da frota
    const varsFrota = variacoes(normFrota)
    const distMin = Math.min(...varsLida.flatMap(vl => varsFrota.map(vf => distanciaLevenshtein(vl, vf))))

    if (distMin < menorDist) {
      menorDist = distMin
      melhor = v
      placaCorrigida = v.placa
    }
  }

  if (menorDist <= 2 && melhor) {
    return { status: 'provavel', veiculo: melhor, placaCorrigida }
  }

  return { status: 'nao_identificada' }
}
